def _load_markdown(mod_type: str):
    doc_file = _MODIFIER_DOC_FILES.get(mod_type)
    if not doc_file:
        return None

    doc_path = os.path.join(_DOC_DIR, doc_file)
    if not os.path.exists(doc_path):
        return None

    with open(doc_path, "r", encoding="utf-8") as f:
        return f.read()


def _extract_modifier_doc(markdown: str, doc_key: str):
    pattern = (
        rf"^## [^\n]*[\uFF08(]{re.escape(doc_key)}[\uFF09)][^\n]*\n"
        rf".*"
    )
    match = re.search(pattern, markdown, flags=re.MULTILINE | re.DOTALL)
    if not match:
        return None
    return match.group(0).strip()


def _extract_param_doc(section_markdown: str, param_name: str):
    escaped_name = re.escape(param_name)
    pattern = (
        rf"^###\s+[^\n]*[\uFF08(]{escaped_name}[\uFF09)][^\n]*\n"
        rf".*?(?=^###\s+|^##\s+|\Z)"
    )
    match = re.search(pattern, section_markdown, flags=re.MULTILINE | re.DOTALL)
    if not match:
        return None
    return match.group(0).strip()


def get_modifier_doc(mod_type: str):
    doc_key = _MODIFIER_DOC_KEYS.get(mod_type)
    if not doc_key:
        return None, None

    markdown = _load_markdown(mod_type)
    if not markdown:
        return None, None

    section = _extract_modifier_doc(markdown, doc_key)
    if not section:
        return None, None

    return doc_key, section


def get_parameter_doc(mod_type: str, param_name: str):
    doc_key, section = get_modifier_doc(mod_type)
    if not section:
        return None, None, None

    mapped_name = _PROPERTY_DOC_NAME_MAP.get(mod_type, {}).get(param_name, param_name)
    parameter_markdown = _extract_param_doc(section, mapped_name)
    if not parameter_markdown:
        return doc_key, None, None

    return doc_key, mapped_name, parameter_markdown


def _format_display_heading(display_name: str):
    label = _tr(display_name).strip()
    if not label:
        return ""

    return re.sub(
        r"\s+\(([^()]*)\)\s*$",
        lambda match: "\uFF08" + match.group(1) + "\uFF09",
        label,
    )


def _override_markdown_heading(markdown: str, display_name: str):
    heading_label = _format_display_heading(display_name)
    if not markdown or not heading_label:
        return markdown

    return re.sub(
        r"^###\s+.+$",
        f"### {heading_label}",
        markdown,
        count=1,
        flags=re.MULTILINE,
    )


