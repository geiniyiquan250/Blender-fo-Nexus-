def _tr(text):
    if nexus_translations is not None:
        zh_cn = getattr(nexus_translations, "zh_CN", None)
        if isinstance(zh_cn, dict):
            for key in (("*", text), ("UI", text), ("Property", text), ("Operator", text)):
                translated = zh_cn.get(key)
                if isinstance(translated, str) and translated:
                    return translated
    translated = bpy.app.translations.pgettext_iface(text)
    return translated if translated else text


