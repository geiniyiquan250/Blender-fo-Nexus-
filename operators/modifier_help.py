def _is_active_pipeline_folder(context):
    scene = getattr(context, "scene", None)
    pipeline = getattr(scene, "nexus_pipeline", None)
    if pipeline is None:
        return False

    index = getattr(pipeline, "modifier_order_index", -1)
    items = getattr(pipeline, "modifier_order", ())
    if not (0 <= index < len(items)):
        return False

    return bool(getattr(items[index], "is_folder", False))


def _get_folder_help_target(context):
    button_prop = getattr(context, "button_prop", None)
    button_pointer = getattr(context, "button_pointer", None)
    pointer_rna = getattr(button_pointer, "bl_rna", None)
    pointer_type = getattr(pointer_rna, "identifier", "")
    if button_prop is not None:
        prop_name = getattr(button_prop, "identifier", "") or getattr(button_prop, "name", "")
        if prop_name in _PIPELINE_FOLDER_PROP_NAMES and _is_active_pipeline_folder(context):
            return "NX_FOLDER", prop_name

    if pointer_type == "NexusPipelineItem" and getattr(button_pointer, "is_folder", False):
        if button_prop is not None:
            param_name = getattr(button_prop, "identifier", "") or getattr(button_prop, "name", "")
        else:
            param_name = _get_button_operator_doc_name(context)
        if param_name:
            return "NX_FOLDER", param_name

    if _is_active_pipeline_folder(context):
        return "NX_FOLDER", "__folder__"

    button_operator = getattr(context, "button_operator", None)
    if button_operator is None:
        return None, None

    identifiers = _get_button_operator_identifiers(button_operator)
    if not any(op_id in _PIPELINE_OPERATOR_IDS for op_id in identifiers):
        return None, None

    param_name = _get_button_operator_doc_name(context)
    if not param_name:
        return None, None

    if param_name in {"Add Folder", "Folder Settings"}:
        return "NX_FOLDER", param_name

    if getattr(button_operator, "is_folder", False) or _is_active_pipeline_folder(context):
        return "NX_FOLDER", param_name

    return None, None


